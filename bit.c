#include <stdio.h>

int main(void)
{
	char bytes[2] = {0};

	bytes[10/8] |= (1 << 10%8);
	int i = 0;
	while(i < 16)
	{
		printf("%i ", (bytes[i/8] >> i%8) & 1);
		i++;
	}
	return 0;
}
