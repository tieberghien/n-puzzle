#include "libft.h"

char	*ft_strjoinfree(char *s1, char *s2, char control)
{
	char	*tmp;

	tmp = ft_strjoin(s1, s2);
	if (control == 'L' || control == 'B')
		free(s1);
	if (control == 'R' || control == 'B')
		free(s2);
	return (tmp);
}
